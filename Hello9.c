#include<stdio.h>

int main(){
    /*This is how for loop can be used*/
    for(float i=1.0; i<=5.0; i++){
        printf("%f\n",i);
    }

    for (char ch = 'a'; ch <= 'f'; ch++)
    {
        printf("%c\n",ch);
    }
    

    int i=1;
    while(i<=5){
        printf("Hello World\n");
        i++;
    }









    return 0;
}