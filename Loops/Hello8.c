# include<stdio.h>
# include<math.h>
int main() {
    //There are all about increment operator
    // ++i (pre increment)
    // i++ (post increment)

    int i=1;
    printf("%d\n", i++); // use, then increase
    printf("%d\n",i);

    printf("%d\n", ++i); // increase, then use
    printf("%d\n",i);
    
     //There are all about decrement operator
    // --i (pre decrement)
    // i-- (post decrement)
    
    printf("%d\n", i--); // use, then decrease
    printf("%d\n",i);

    printf("%d\n", --i); // decrease, then use
    printf("%d\n",i);
    
 
  
   
    
    return 0;
} 